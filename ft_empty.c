#include "cub3d.h"

int		ft_empty(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] != '\n' || str[i] != '\0' || str[i] != ' ')
			return (0);
		i++;
	}
	return (1);
}
