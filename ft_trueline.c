#include "cub3d.h"

int		ft_trueline(char *str)
{
	int	i;

	i = 0;
	while (str[i] == ' ')
		i++;
	if ((str[i] == 'R') || (str[i] == 'N' && str[i + 1] == 'O') ||
			(str[i] == 'S' && str[i + 1] == 'O') ||
			(str[i] == 'W' && str[i + 1] == 'E') ||
			(str[i] == 'E' && str[i + 1] == 'A') ||
			(str[i] == 'S') || (str[i] == 'F') || (str[i] == 'C'))
		return (1);
	return (0);
}
