#include "cub3d.h"

void	read_map(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		g_info.map[g_rows][i] = str[i];
		i++;
	}
	if (ft_search_map(str) == 0)
		g_error = 1;
	g_rows++;
}
